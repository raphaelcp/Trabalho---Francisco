import numpy as np

def convert_crisp(U):
	n = len(U)
	K = len(U[0])
	cisp_matrix = np.zeros((n, K), dtype=int)

	for i in range(n):
		k_max = 0
		for k in range(K):
			if (U[i][k] > U[i][k_max]):
				k_max = k
		cisp_matrix[i][k_max]	= 1

	return cisp_matrix

